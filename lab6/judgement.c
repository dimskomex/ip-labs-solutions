#include <stdio.h>
#include <limits.h>

/* 
 * Για να τρέξετε το πρόγραμμα με ανακατεύθυνση εισόδου από το αρχείο grades.txt:
 * gcc judgement.c -o judgement
 * ./judgement < grades.txt
 */


/*
 * Συνάρτηση που βρίσκει το μέγιστο στοιχείο ενός πίνακα ακεραίων.
 * array: δείκτης στον πίνακα
 * n: πλήθος στοιχείων του πίνακα
 * Επιστρέφει τη μεγαλύτερη τιμή που υπάρχει στον πίνακα.
 */
int max_array(int *array, int n) {
    int max = INT_MIN;   // Αρχικοποίηση με τη μικρότερη δυνατή τιμή int
    int i;

    // Διατρέχουμε όλα τα στοιχεία του πίνακα
    for (i = 0; i < n; ++i) {
        // Αν βρεθεί μεγαλύτερο στοιχείο, ενημερώνουμε το max
        if (array[i] > max) {
            max = array[i];
        }
    }

    return max;
}

/*
 * Συνάρτηση που βρίσκει το ελάχιστο στοιχείο ενός πίνακα ακεραίων.
 * array: δείκτης στον πίνακα
 * n: πλήθος στοιχείων του πίνακα
 * Επιστρέφει τη μικρότερη τιμή που υπάρχει στον πίνακα.
 */
int min_array(int *array, int n) {
    int min = INT_MAX;   // Αρχικοποίηση με τη μεγαλύτερη δυνατή τιμή int
    int i;

    // Διατρέχουμε όλα τα στοιχεία του πίνακα
    for (i = 0; i < n; ++i) {
        // Αν βρεθεί μικρότερο στοιχείο, ενημερώνουμε το min
        if (array[i] < min) {
            min = array[i];
        }
    }

    return min;
}

/*
 * Συνάρτηση που υπολογίζει το άθροισμα των στοιχείων ενός πίνακα ακεραίων.
 * array: δείκτης στον πίνακα
 * n: πλήθος στοιχείων του πίνακα
 * Επιστρέφει το άθροισμα όλων των στοιχείων.
 */
int sum_array(int *array, int n) {
    int sum = 0;   // Μεταβλητή για το άθροισμα
    int i;

    // Προσθέτουμε όλα τα στοιχεία του πίνακα
    for (i = 0; i < n; ++i) {
        sum += array[i];
    }

    return sum;
}

int main(void) {
    int grades[10];   // Πίνακας που αποθηκεύει 10 βαθμούς
    int min, max, sum;
    float average;
    int i;

    /*
     * Εισαγωγή των 10 βαθμών από το πληκτρολόγιο.
     */
    for (i = 0; i < 10; ++i) {
        scanf("%d", &grades[i]);
    }

    /*
     * Υπολογισμός:
     *  - αθροίσματος όλων των βαθμών
     *  - ελάχιστου βαθμού
     *  - μέγιστου βαθμού
     */
    sum = sum_array(grades, 10);
    min = min_array(grades, 10);
    max = max_array(grades, 10);

    /*
     * Υπολογισμός του μέσου όρου:
     * Αφαιρούμε τον μικρότερο και τον μεγαλύτερο βαθμό
     * από το συνολικό άθροισμα και διαιρούμε με το 8
     * (τους υπόλοιπους βαθμούς).
     * Το 8.0 χρησιμοποιείται ώστε η διαίρεση να είναι δεκαδική.
     */
    average = (sum - min - max) / 8.0;

    // Εκτύπωση των αποτελεσμάτων
    printf("Sum: %d\n", sum);
    printf("Min: %d\n", min);
    printf("Max: %d\n", max);
    printf("Average: %.2f\n", average);

    return 0;
}
